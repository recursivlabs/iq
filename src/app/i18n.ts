function normalizeLocale(value: string | null | undefined): LocaleKey | null {
  if (!value) return null;
  const code = value.toLowerCase().split('-')[0];
  return supportedLocales.includes(code as LocaleKey) ? code as LocaleKey : null;
}

export function detectBrowserLocale(): LocaleKey {
  if (typeof window === 'undefined') return 'en';

  const urlLocale = normalizeLocale(new URLSearchParams(window.location.search).get('lang'));
  if (urlLocale) return urlLocale;

  const languages = navigator.languages && navigator.languages.length > 0
    ? navigator.languages
    : [navigator.language];
  for (const language of languages) {
    const locale = normalizeLocale(language);
    if (locale) return locale;
  }
  return 'en';
}

export function localeLabel(locale: LocaleKey) {
  return localeLabels[locale];
}

export function translate(locale: LocaleKey, text: string) {
  if (locale === 'en') return text;
  return translations[locale]?.[text] || text;
}
